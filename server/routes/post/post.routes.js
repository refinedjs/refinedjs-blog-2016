(function() {
	"use strict";
//================================================================================//
//=========  include modules =====================================================//
//================================================================================//
	var controller;
	controller = require( "./post.controller" );
//================================================================================//
//========= export routes  =======================================================//
//================================================================================//
	module.exports = setPostRoutes;	
//================================================================================//
//=========  routes  =============================================================//
//================================================================================//
	/*
	**-------------------------------------------------------------------------------------
	** METHOD NAME - setRoutes
	** DESC - container for all the routes for this module 
	**-------------------------------------------------------------------------------------
	*/
	function setPostRoutes( app ) {
		app.route( "/posts/:date/:post" ).get( controller.getPost );
	}
}());